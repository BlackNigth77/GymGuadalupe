package com.example.gymguadalupe;

public class Pagos {
    private String id_pago;
    private String email_pago;
    private String membresia_pago;
    private String fecha_registro_pago;
    private String fecha_vencimiento_pago;
    private String costo_pago;

    public String getId_pago() {
        return id_pago;
    }

    public void setId_pago(String id_pago) {
        this.id_pago = id_pago;
    }

    public String getEmail_pago() {
        return email_pago;
    }

    public void setEmail_pago(String email_pago) {
        this.email_pago = email_pago;
    }

    public String getMembresia_pago() {
        return membresia_pago;
    }

    public void setMembresia_pago(String membresia_pago) {
        this.membresia_pago = membresia_pago;
    }

    public String getFecha_registro_pago() {
        return fecha_registro_pago;
    }

    public void setFecha_registro_pago(String fecha_registro_pago) {
        this.fecha_registro_pago = fecha_registro_pago;
    }

    public String getFecha_vencimiento_pago() {
        return fecha_vencimiento_pago;
    }

    public void setFecha_vencimiento_pago(String fecha_vencimiento_pago) {
        this.fecha_vencimiento_pago = fecha_vencimiento_pago;
    }
    public String getCosto_pago() {
        return costo_pago;
    }

    public void setCosto_pago(String costo_pago) {
        this.costo_pago = costo_pago;
    }
}
