package com.example.gymguadalupe;

public class Socio {

    private String id_socio;
    private String NombreSocio;
    private String ApellidosSocio;
    private String CelularSocio;
    private String DireccionSocio;
    private String FNSocio;

    public String getId_socio() {
        return id_socio;
    }

    public void setId_socio(String id_socio) {
        this.id_socio = id_socio;
    }

    public String getNombreSocio() {
        return NombreSocio;
    }

    public void setNombreSocio(String nombreSocio) {
        NombreSocio = nombreSocio;
    }

    public String getApellidosSocio() {
        return ApellidosSocio;
    }

    public void setApellidosSocio(String apellidosSocio) {
        ApellidosSocio = apellidosSocio;
    }

    public String getCelularSocio() {
        return CelularSocio;
    }

    public void setCelularSocio(String celularSocio) {
        CelularSocio = celularSocio;
    }

    public String getDireccionSocio() {
        return DireccionSocio;
    }

    public void setDireccionSocio(String direccionSocio) {
        DireccionSocio = direccionSocio;
    }

    public String getFNSocio() {
        return FNSocio;
    }

    public void setFNSocio(String FNSocio) {
        this.FNSocio = FNSocio;
    }
}
