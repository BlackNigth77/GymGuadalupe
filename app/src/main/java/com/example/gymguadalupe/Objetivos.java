package com.example.gymguadalupe;

public class Objetivos {
    private String id_objetivo;
    private String espalda_objetivo;
    private String cadera_objetivo;
    private String peso_objetivo;
    private String IMC_objetivo;
    private String cintura_objetivo;
    private String fecha_registro_objetivo;
    private String user_objetivo;

    public String getId_objetivo() {
        return id_objetivo;
    }

    public void setId_objetivo(String id_objetivo) {
        this.id_objetivo = id_objetivo;
    }

    public String getEspalda_objetivo() {
        return espalda_objetivo;
    }

    public void setEspalda_objetivo(String espalda_objetivo) {
        this.espalda_objetivo = espalda_objetivo;
    }

    public String getCadera_objetivo() {
        return cadera_objetivo;
    }

    public void setCadera_objetivo(String cadera_objetivo) {
        this.cadera_objetivo = cadera_objetivo;
    }

    public String getPeso_objetivo() {
        return peso_objetivo;
    }

    public void setPeso_objetivo(String peso_objetivo) {
        this.peso_objetivo = peso_objetivo;
    }

    public String getIMC_objetivo() {
        return IMC_objetivo;
    }

    public void setIMC_objetivo(String IMC_objetivo) {
        this.IMC_objetivo = IMC_objetivo;
    }

    public String getCintura_objetivo() {
        return cintura_objetivo;
    }

    public void setCintura_objetivo(String cintura_objetivo) {
        this.cintura_objetivo = cintura_objetivo;
    }

    public String getFecha_registro_objetivo() {
        return fecha_registro_objetivo;
    }

    public void setFecha_registro_objetivo(String fecha_registro_objetivo) {
        this.fecha_registro_objetivo = fecha_registro_objetivo;
    }

    public String getUser_objetivo() {
        return user_objetivo;
    }

    public void setUser_objetivo(String user_objetivo) {
        this.user_objetivo = user_objetivo;
    }

    @Override
    public String toString() {
        return peso_objetivo;
    }

}
