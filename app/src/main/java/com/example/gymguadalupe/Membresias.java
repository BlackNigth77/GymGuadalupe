package com.example.gymguadalupe;

public class Membresias {
    private String id_membresia;
    private String costo_mebresia;
    private String nombre_membresia;
    private String tipo_membresia;

    public String getId_membresia() {
        return id_membresia;
    }

    public void setId_membresia(String id_membresia) {
        this.id_membresia = id_membresia;
    }

    public String getCosto_mebresia() {
        return costo_mebresia;
    }

    public void setCosto_mebresia(String costo_mebresia) {
        this.costo_mebresia = costo_mebresia;
    }

    public String getNombre_membresia() {
        return nombre_membresia;
    }

    public void setNombre_membresia(String nombre_membresia) {
        this.nombre_membresia = nombre_membresia;
    }

    public String getTipo_membresia() {
        return tipo_membresia;
    }

    public void setTipo_membresia(String tipo_membresia) {
        this.tipo_membresia = tipo_membresia;
    }
}

